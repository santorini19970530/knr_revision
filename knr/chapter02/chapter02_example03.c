/* Example 2.3: Variable declarations */

#include <stdio.h>

int main() {
    int lower, upper, step;

    lower = 0; upper = 100; step = 10;

    printf("lower = %d, upper = %d, step = %d\n", lower, upper, step);

    return 0;
} 
